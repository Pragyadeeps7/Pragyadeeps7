const TAGS_OPTIONS = [
  ["new"], ["sale"], ["festive"], ["new", "festive"], ["sale", "bestseller"], ["bestseller"], ["luxe"], ["new", "bestseller"]
];

function makeProduct(idx, key, name, cat) {
  const basePrice = 1500 + ((idx * 137) % 23000);
  const hasSale = idx % 3 === 0;
  const price = hasSale ? Math.round(basePrice * 0.78) : basePrice;
  const compare = hasSale ? basePrice : null;
  const rating = (4 + ((idx * 7) % 9) / 10).toFixed(1);
  const reviews = 24 + (idx * 13) % 480;
  const tags = [...TAGS_OPTIONS[idx % TAGS_OPTIONS.length]];
  if (hasSale) tags.push("sale");
  return {
    id: `p-${idx + 1}`,
    name,
    category: cat,
    image: PRODUCT_IMAGES[key],
    price,
    compareAtPrice: compare,
    rating: parseFloat(rating),
    reviews,
    tags: Array.from(new Set(tags)),
    colors: ["#D4A574", "#1A1A1A", "#F5F5F5", "#8B5A3C"].slice(0, 2 + (idx % 3)),
    sizes: ["S", "M", "L"],
    description:
      "Hand-crafted by skilled artisans, this piece marries old-world craftsmanship with contemporary sensibility. Each one is subtly unique  imperfections are part of its story.",
  };
}

export const PRODUCTS = (() => {
  const list = [];
  let idx = 0;
  // create 8 of each template => 8*16 = 128
  for (let t of TEMPLATES) {
    for (let i = 0; i < 8; i++) {
      const name = t.names[i % t.names.length] + (i >= t.names.length ? ` 0${i - t.names.length + 2}` : "");
      list.push(makeProduct(idx, t.key, name, t.cat));
      idx++;
    }
  }
  return list;
})();

